package br.com.le.addon.qualitymanagement.model;


public class FechaPlpVariosServicos {
    String xml;

    String idPlpCliente;

    String cartaoPostagem;

    String listaEtiquetas;

    public String getXml() {
        return this.xml;
    }

    public void setXml(String xml) {
        this.xml = xml;
    }

    public String getIdPlpCliente() {
        return this.idPlpCliente;
    }

    public void setIdPlpCliente(String idPlpCliente) {
        this.idPlpCliente = idPlpCliente;
    }

    public String getCartaoPostagem() {
        return this.cartaoPostagem;
    }

    public void setCartaoPostagem(String cartaoPostagem) {
        this.cartaoPostagem = cartaoPostagem;
    }

    public String getListaEtiquetas() {
        return this.listaEtiquetas;
    }

    public void setListaEtiquetas(String listaEtiquetas) {
        this.listaEtiquetas = listaEtiquetas;
    }
}
